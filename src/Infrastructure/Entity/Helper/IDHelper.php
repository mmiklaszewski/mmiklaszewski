<?php

namespace App\Infrastructure\Entity\Helper;

use Doctrine\ORM\Mapping as ORM;

trait IDHelper
{
    #[ORM\Id]
    #[ORM\GeneratedValue]
    #[ORM\Column]
    private ?int $id = null;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function setId(?int $id): void
    {
        $this->id = $id;
    }
}
